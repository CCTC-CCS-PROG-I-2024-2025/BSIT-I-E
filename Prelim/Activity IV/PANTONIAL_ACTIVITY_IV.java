class PANTONIAL_ACTIVITY_IV {
    public static void main (String[] args) {
        int x = 6;
        int y = 4;
        int z = 3;

        int sum = x + y;
        int difference = y - z;
        int product = z * x;
        int quotient = y / x;

        System.out.println(sum);
        System.out.println(difference);
        System.out.println(product);
        System.out.println(quotient);
    }
}
